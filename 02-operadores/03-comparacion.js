let a = 10;
console.log(a > 5); // cuando se usa operador de comparacion no da un booleano (true/false)
console.log(a >=5);
console.log(a <5);
console.log(a <=5);

//operadores de igualdad

console.log(a == 10); //estamos evaluando si es que la variable a es exactamente al numero 10
console.log(a != 10);
console.log(a == '10');// el == doble igualdad solo va a preguntar si los valores son iguales, pero no pregunta si los tipos son iguales  
console.log(a === '10');//la triple igualdad compara el valor y el tipo de dato 
console.log(a !== '10');//esta pregunta si son distinto tipo de dato